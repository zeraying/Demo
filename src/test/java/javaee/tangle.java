package javaee;

public class tangle {
    private double wide;
    private double length;

    public tangle() {

    }

    public tangle(double w, double l) {
        wide = w;
        length = l;
    }

    public void setWide(double w) {
        wide = w;
    }

    public double getWide() {
        return wide;
    }
    public void setLength(double l){
        length = l;
    }
    public double getLength(){
        return length;
    }
}
